/// Wrapper for returning raw bytes as a Lua string from methods that lack
/// direct `Lua` access (i.e. those generated by `#[harmony_macros::implementation]`).
struct LuaBytes(Vec<u8>);

impl IntoLua for LuaBytes {
    fn into_lua(self, lua: &Lua) -> mlua::Result<Value> {
        Ok(Value::String(lua.create_string(&self.0)?))
    }
}

impl LuauTypeInfo for LuaBytes {
    fn luau_type() -> LuauType {
        String::luau_type()
    }
}

#[derive(Clone, Debug, Deserialize, Serialize)]
#[harmony_macros::interface]
struct SocketAddress {
    /// IP address (e.g. "192.168.1.1").
    address: String,
    /// Port number.
    port: u16,
}

impl FromLua for SocketAddress {
    fn from_lua(value: Value, lua: &Lua) -> mlua::Result<Self> {
        lua.from_value(value)
    }
}

impl IntoLua for SocketAddress {
    fn into_lua(self, lua: &Lua) -> mlua::Result<Value> {
        lua.to_value(&self)
    }
}

impl From<SocketAddr> for SocketAddress {
    fn from(addr: SocketAddr) -> Self {
        Self {
            address: addr.ip().to_string(),
            port: addr.port(),
        }
    }
}

impl SocketAddress {
    fn to_socket_addr(&self) -> mlua::Result<SocketAddr> {
        let ip: std::net::IpAddr = self.address.parse().map_err(|e| {
            mlua::Error::runtime(format!("invalid IP address '{}': {e}", self.address))
        })?;
        Ok(SocketAddr::new(ip, self.port))
    }
}

