const MAX_READ_BYTES: usize = 8 * 1024 * 1024; // 8 MB
const UDP_MAX_DATAGRAM: usize = 65535;

fn validate_timeout(secs: f64) -> mlua::Result<Duration> {
    if secs <= 0.0 || secs.is_nan() || secs.is_infinite() {
        return Err(mlua::Error::runtime(
            "timeout must be a finite number greater than 0",
        ));
    }
    Ok(Duration::from_secs_f64(secs))
}

fn set_timeout(timeout_mutex: &StdMutex<Option<f64>>, timeout: Option<f64>) -> mlua::Result<()> {
    if let Some(secs) = timeout {
        validate_timeout(secs)?;
    }
    let mut guard = timeout_mutex.lock().unwrap_or_else(|e| e.into_inner());
    *guard = timeout;
    Ok(())
}

fn effective_timeout(
    per_op: Option<f64>,
    socket_default: &StdMutex<Option<f64>>,
) -> mlua::Result<Option<Duration>> {
    let secs = per_op.or_else(|| *socket_default.lock().unwrap_or_else(|e| e.into_inner()));
    match secs {
        Some(s) => Ok(Some(validate_timeout(s)?)),
        None => Ok(None),
    }
}

async fn with_optional_timeout<F, T>(timeout: Option<Duration>, fut: F) -> mlua::Result<T>
where
    F: std::future::Future<Output = mlua::Result<T>>,
{
    match timeout {
        Some(duration) => tokio::time::timeout(duration, fut)
            .await
            .map_err(|_| mlua::Error::runtime("operation timed out"))?,
        None => fut.await,
    }
}

#[derive(Clone)]
struct UdpSocket {
    // RwLock allows concurrent I/O (read) while connect/close take exclusive (write) access.
    inner: Arc<TokioRwLock<Option<Arc<TokioUdpSocket>>>>,
    timeout: Arc<StdMutex<Option<f64>>>,
    local_addr: Arc<StdMutex<SocketAddress>>,
}

#[harmony_macros::implementation]
impl UdpSocket {
    /// Sets the default timeout in seconds for all subsequent operations. Pass nil to clear.
    pub fn set_timeout(&self, seconds: Option<f64>) -> mlua::Result<()> {
        set_timeout(&self.timeout, seconds)
    }

    /// Returns the local address this socket is bound to. After `connect`, reflects the
    /// interface used to reach the peer.
    pub fn address(&self) -> SocketAddress {
        self.local_addr
            .lock()
            .unwrap_or_else(|e| e.into_inner())
            .clone()
    }

    /// Associates the socket with a remote address. After connecting, use `send` and `recv`
    /// instead of `send_to` and `recv_from`. Also updates `address()` to reflect the local
    /// interface used to reach the peer.
    pub async fn connect(&self, address: SocketAddress) -> mlua::Result<()> {
        let guard = self.inner.write().await;
        let socket = guard
            .as_ref()
            .ok_or_else(|| mlua::Error::runtime("socket is closed"))?;
        let addr = address.to_socket_addr()?;
        socket
            .connect(addr)
            .await
            .map_err(|e| mlua::Error::runtime(format!("connect failed: {e}")))?;

        let local = socket
            .local_addr()
            .map(SocketAddress::from)
            .map_err(|e| mlua::Error::runtime(format!("failed to get local address: {e}")))?;
        *self.local_addr.lock().unwrap_or_else(|e| e.into_inner()) = local;
        Ok(())
    }

    /// Sends a datagram on a connected socket. Call `connect` first.
    #[harmony(args(data: String, timeout: Option<f64>))]
    pub async fn send(&self, data: mlua::String, timeout: Option<f64>) -> mlua::Result<()> {
        let guard = self.inner.read().await;
        let socket = guard
            .as_ref()
            .ok_or_else(|| mlua::Error::runtime("socket is closed"))?;
        let timeout = effective_timeout(timeout, &self.timeout)?;
        // Copy bytes out of the mlua::String — it is not Send.
        let bytes = data.as_bytes().to_vec();

        with_optional_timeout(timeout, async {
            socket
                .send(&bytes)
                .await
                .map_err(|e| mlua::Error::runtime(format!("send failed: {e}")))?;
            Ok(())
        })
        .await
    }

    /// Receives a datagram on a connected socket. Call `connect` first.
    pub async fn recv(&self, timeout: Option<f64>) -> mlua::Result<LuaBytes> {
        let guard = self.inner.read().await;
        let socket = guard
            .as_ref()
            .ok_or_else(|| mlua::Error::runtime("socket is closed"))?;
        let timeout = effective_timeout(timeout, &self.timeout)?;

        let mut buf = vec![0u8; UDP_MAX_DATAGRAM];
        let n = with_optional_timeout(timeout, async {
            socket
                .recv(&mut buf)
                .await
                .map_err(|e| mlua::Error::runtime(format!("recv failed: {e}")))
        })
        .await?;

        buf.truncate(n);
        Ok(LuaBytes(buf))
    }

    /// Receives a datagram. Returns the data and the sender address. Yields until data arrives.
    #[harmony(returns(LuaBytes, SocketAddress))]
    pub async fn recv_from(&self, timeout: Option<f64>) -> mlua::Result<(LuaBytes, SocketAddress)> {
        let guard = self.inner.read().await;
        let socket = guard
            .as_ref()
            .ok_or_else(|| mlua::Error::runtime("socket is closed"))?;
        let timeout = effective_timeout(timeout, &self.timeout)?;

        let mut buf = vec![0u8; UDP_MAX_DATAGRAM];
        let (n, addr) = with_optional_timeout(timeout, async {
            socket
                .recv_from(&mut buf)
                .await
                .map_err(|e| mlua::Error::runtime(format!("recv_from failed: {e}")))
        })
        .await?;

        buf.truncate(n);
        Ok((LuaBytes(buf), SocketAddress::from(addr)))
    }

    /// Sends a datagram to the specified address.
    #[harmony(args(data: String, address: SocketAddress, timeout: Option<f64>))]
    pub async fn send_to(
        &self,
        data: mlua::String,
        address: SocketAddress,
        timeout: Option<f64>,
    ) -> mlua::Result<()> {
        let guard = self.inner.read().await;
        let socket = guard
            .as_ref()
            .ok_or_else(|| mlua::Error::runtime("socket is closed"))?;
        let timeout = effective_timeout(timeout, &self.timeout)?;
        let addr = address.to_socket_addr()?;
        // Copy bytes out of the mlua::String — it is not Send.
        let bytes = data.as_bytes().to_vec();

        with_optional_timeout(timeout, async {
            socket
                .send_to(&bytes, addr)
                .await
                .map_err(|e| mlua::Error::runtime(format!("send_to failed: {e}")))?;
            Ok(())
        })
        .await
    }

    /// Closes the socket. Further operations will throw.
    pub async fn close(&self) -> mlua::Result<()> {
        let mut guard = self.inner.write().await;
        *guard = None;
        Ok(())
    }
}

