#[derive(Clone)]
struct TcpStream {
    // No inner Arc — read/write need &mut self, so access is serialized.
    inner: Arc<TokioMutex<Option<TokioTcpStream>>>,
    timeout: Arc<StdMutex<Option<f64>>>,
    local_addr: SocketAddress,
}

#[harmony_macros::implementation]
impl TcpStream {
    /// Sets the default timeout in seconds for all subsequent operations. Pass nil to clear.
    pub fn set_timeout(&self, seconds: Option<f64>) -> mlua::Result<()> {
        set_timeout(&self.timeout, seconds)
    }

    /// Returns the local address this socket is bound to.
    pub fn address(&self) -> SocketAddress {
        self.local_addr.clone()
    }

    /// Reads up to max_bytes (max 8 MB). Yields until at least 1 byte arrives. Returns empty string on EOF.
    pub async fn read(&self, max_bytes: usize, timeout: Option<f64>) -> mlua::Result<LuaBytes> {
        if max_bytes == 0 {
            return Err(mlua::Error::runtime("max_bytes must be greater than 0"));
        }
        if max_bytes > MAX_READ_BYTES {
            return Err(mlua::Error::runtime(format!(
                "max_bytes exceeds limit of {MAX_READ_BYTES} (8 MB)"
            )));
        }

        let timeout = effective_timeout(timeout, &self.timeout)?;
        let mut guard = self.inner.lock().await;
        let stream = guard
            .as_mut()
            .ok_or_else(|| mlua::Error::runtime("stream is closed"))?;

        let mut buf = vec![0u8; max_bytes];
        let n = with_optional_timeout(timeout, async {
            stream
                .read(&mut buf)
                .await
                .map_err(|e| mlua::Error::runtime(format!("read failed: {e}")))
        })
        .await?;

        buf.truncate(n);
        Ok(LuaBytes(buf))
    }

    /// Writes all bytes to the stream. Yields until fully written.
    #[harmony(args(data: String, timeout: Option<f64>))]
    pub async fn write(&self, data: mlua::String, timeout: Option<f64>) -> mlua::Result<()> {
        let timeout = effective_timeout(timeout, &self.timeout)?;
        let mut guard = self.inner.lock().await;
        let stream = guard
            .as_mut()
            .ok_or_else(|| mlua::Error::runtime("stream is closed"))?;

        // Copy bytes out of the mlua::String — it is not Send.
        let bytes = data.as_bytes().to_vec();
        with_optional_timeout(timeout, async {
            stream
                .write_all(&bytes)
                .await
                .map_err(|e| mlua::Error::runtime(format!("write failed: {e}")))
        })
        .await
    }

    /// Closes the stream. Further operations will throw.
    pub async fn close(&self) -> mlua::Result<()> {
        let mut guard = self.inner.lock().await;
        if let Some(mut stream) = guard.take() {
            // Graceful shutdown (FIN). Errors are ignored — the stream
            // is being dropped regardless.
            let _ = stream.shutdown().await;
        }
        Ok(())
    }
}

harmony_macros::compile!(type_path = TcpStream, methods = true);

#[derive(Clone)]
struct TcpListener {
    inner: Arc<TokioMutex<Option<Arc<TokioTcpListener>>>>,
    timeout: Arc<StdMutex<Option<f64>>>,
    local_addr: SocketAddress,
}

#[harmony_macros::implementation]
impl TcpListener {
    /// Sets the default timeout in seconds for all subsequent operations. Pass nil to clear.
    pub fn set_timeout(&self, seconds: Option<f64>) -> mlua::Result<()> {
        set_timeout(&self.timeout, seconds)
    }

    /// Returns the local address this socket is bound to.
    pub fn address(&self) -> SocketAddress {
        self.local_addr.clone()
    }

    /// Accepts a new connection. Yields until a client connects. Returns the stream and the client address.
    #[harmony(returns(TcpStream, SocketAddress))]
    pub async fn accept(&self, timeout: Option<f64>) -> mlua::Result<(TcpStream, SocketAddress)> {
        let listener = {
            let guard = self.inner.lock().await;
            guard
                .as_ref()
                .cloned()
                .ok_or_else(|| mlua::Error::runtime("listener is closed"))?
        };
        let timeout = effective_timeout(timeout, &self.timeout)?;

        let (stream, addr) = with_optional_timeout(timeout, async {
            listener
                .accept()
                .await
                .map_err(|e| mlua::Error::runtime(format!("accept failed: {e}")))
        })
        .await?;

        let local_addr = stream
            .local_addr()
            .map(SocketAddress::from)
            .map_err(|e| mlua::Error::runtime(format!("failed to get local address: {e}")))?;
        let tcp_stream = TcpStream {
            inner: Arc::new(TokioMutex::new(Some(stream))),
            timeout: Arc::new(StdMutex::new(None)),
            local_addr,
        };
        Ok((tcp_stream, SocketAddress::from(addr)))
    }

    /// Closes the listener. Further operations will throw.
    pub async fn close(&self) -> mlua::Result<()> {
        let mut guard = self.inner.lock().await;
        *guard = None;
        Ok(())
    }
}

harmony_macros::compile!(type_path = TcpListener, methods = true);

fn parse_bind_address(address: &str, port: u16) -> mlua::Result<SocketAddr> {
    let ip: std::net::IpAddr = address
        .parse()
        .map_err(|e| mlua::Error::runtime(format!("invalid bind address '{address}': {e}")))?;
    Ok(SocketAddr::new(ip, port))
}

async fn udp_bind(options: UdpBindOptions) -> mlua::Result<UdpSocket> {
    let addr = parse_bind_address(&options.address, options.port)?;

    let socket = TokioUdpSocket::bind(addr)
        .await
        .map_err(|e| mlua::Error::runtime(format!("failed to bind UDP socket: {e}")))?;

    if options.broadcast.unwrap_or(false) {
        socket
            .set_broadcast(true)
            .map_err(|e| mlua::Error::runtime(format!("failed to enable broadcast: {e}")))?;
    }

    let local_addr = socket
        .local_addr()
        .map(SocketAddress::from)
        .map_err(|e| mlua::Error::runtime(format!("failed to get local address: {e}")))?;

    Ok(UdpSocket {
        inner: Arc::new(TokioRwLock::new(Some(Arc::new(socket)))),
        timeout: Arc::new(StdMutex::new(None)),
        local_addr: Arc::new(StdMutex::new(local_addr)),
    })
}

async fn tcp_connect(options: TcpConnectOptions) -> mlua::Result<TcpStream> {
    let addr = format!("{}:{}", options.host, options.port);

    let timeout = options.timeout.map(validate_timeout).transpose()?;
    let connect_fut = TokioTcpStream::connect(&addr);
    let stream = match timeout {
        Some(duration) => tokio::time::timeout(duration, connect_fut)
            .await
            .map_err(|_| mlua::Error::runtime("connection timed out"))?,
        None => connect_fut.await,
    }
    .map_err(|e| mlua::Error::runtime(format!("failed to connect: {e}")))?;

    let local_addr = stream
        .local_addr()
        .map(SocketAddress::from)
        .map_err(|e| mlua::Error::runtime(format!("failed to get local address: {e}")))?;

    Ok(TcpStream {
        inner: Arc::new(TokioMutex::new(Some(stream))),
        timeout: Arc::new(StdMutex::new(None)),
        local_addr,
    })
}

async fn tcp_bind(options: TcpBindOptions) -> mlua::Result<TcpListener> {
    let addr = parse_bind_address(&options.address, options.port)?;

    let listener = TokioTcpListener::bind(addr)
        .await
        .map_err(|e| mlua::Error::runtime(format!("failed to bind TCP listener: {e}")))?;

    let local_addr = listener
        .local_addr()
        .map(SocketAddress::from)
        .map_err(|e| mlua::Error::runtime(format!("failed to get local address: {e}")))?;

    Ok(TcpListener {
        inner: Arc::new(TokioMutex::new(Some(Arc::new(listener)))),
        timeout: Arc::new(StdMutex::new(None)),
        local_addr,
    })
}

pub fn get_module() -> Module {
    Module {
        path: "harmony/net".into(),
        setup: Arc::new(|lua: &Lua| -> anyhow::Result<mlua::Table> {
            let table = lua.create_table()?;
            table.set(
                "udp_bind",
                lua.create_async_function(|_, options: UdpBindOptions| async move {
                    udp_bind(options).await
                })?,
            )?;
            table.set(
                "tcp_connect",
                lua.create_async_function(|_, options: TcpConnectOptions| async move {
                    tcp_connect(options).await
                })?,
            )?;
            table.set(
                "tcp_bind",
                lua.create_async_function(|_, options: TcpBindOptions| async move {
                    tcp_bind(options).await
                })?,
            )?;
            Ok(table)
        }),
        scope: harmony_core::Scope {
            id: "harmony.net".into(),
            description: "Open raw TCP and UDP sockets.",
            danger: harmony_core::Danger::High,
        },
    }
}

